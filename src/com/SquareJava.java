package com;
import java.lang.Math;

public class SquareJava {

	public static void main(String[] args) {
		//Square formula
		double a=3;
		double b=2;
		System.out.println(Math.pow(a, b));
		// cube formula
		double x=3;
		double y=3;
		System.out.println(Math.pow(x, y));
		//Square root formula
		System.out.println(Math.sqrt(x));
		

	}

}
